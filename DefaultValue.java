//Write a Java program to display the default value of all primitive data types of Java???
public class DefaultValue {
    static byte a;
    static short b;
    static int c;
    static long d;
    static char e;
    static float f;
    static double g;
    static boolean h;

    public static void main(String[] args) {

        System.out.println("Default Valur of byte : " + a);
        System.out.println("Default Valur of short : " + b);
        System.out.println("Default Valur of int : " + c);
        System.out.println("Default Valur of long : " + d);
        System.out.println("Default Valur of char : " + e);
        System.out.println("Default Valur of float : " + f);
        System.out.println("Default Valur of double : " + g);
        System.out.println("Default Valur of boolean : " + h);

    }
}
